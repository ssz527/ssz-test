import utils from '../../src/hdmap/utils.js';

describe('根据车位中心获取车位顶点坐标', () => {
  it('测试点与车位顶点坐标相同', () => {
    expect([
      [
        [-199.53125, 225.15625],
        [-159.53125, 225.15625],
        [-159.53125, 145.15625],
        [-199.53125, 145.15625],
        [-199.53125, 225.15625]
      ]
    ]).to.deep.equal(utils.getParkingCoordinates([-179.53125, 185.15625]));
  });
  it('测试点与车位顶点坐标不同', () => {
    expect([
      [
        [-179.03620993107282, 221.90611955975558],
        [-143.4593303108392, 203.62263968071161],
        [-180.02629006892718, 132.46888044024442],
        [-215.6031696891608, 150.75236031928839],
        [-179.03620993107282, 221.90611955975558]
      ]
    ]).to.not.equal(utils.getParkingCoordinates([-179.53125, 185.15625]));
  });
});
